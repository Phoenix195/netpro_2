    private void displayPageAHyperlinkUpdate(javax.swing.event.HyperlinkEvent evt) {//GEN-FIRST:event_displayPageAHyperlinkUpdate
                        HyperlinkEvent.EventType eventType = evt.getEventType();
        if (eventType == HyperlinkEvent.EventType.ACTIVATED) {
            if (evt instanceof HTMLFrameHyperlinkEvent) {
                HTMLFrameHyperlinkEvent linkEvent =
                        (HTMLFrameHyperlinkEvent) evt;
                HTMLDocument document =
                        (HTMLDocument) displayPageA.getDocument();
                document.processHTMLFrameHyperlinkEvent(linkEvent);
            } else {
                try {
                    displayPageA.setPage(evt.getURL());
                
                    urlNameA.setText(evt.getURL().toString());
                } catch (IOException ex) {
                    Logger.getLogger(MiniBrowserJframe.class.getName()).log(Level.SEVERE, null, ex);
                }

                
            }
             
        }
    }//GEN-LAST:event_displayPageAHyperlinkUpdate

